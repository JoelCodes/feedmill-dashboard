// that import to a no-op so direct Drizzle access from this script is safe.
import { db } from '@/db';
import { productionOrders } from '@/db/schema/orders';
import { orderEvents } from '@/db/schema/events';
import { importBatches } from '@/db/schema/imports';
import { productionOrderImportSchema } from '@/actions/import-schema';
import { MAX_IMPORT_BYTES } from '@/lib/import-constants';

// read-excel-file 9.x requires a module-level import; tsx handles ESM interop.
// The type cast pattern used here matches src/actions/import.ts exactly
// (lines 123-128) to keep the two parse pipelines aligned.
import readXlsxFile from 'read-excel-file/node';

import { eq, sql } from 'drizzle-orm';

// ── Constants ────────────────────────────────────────────────────────────────

/**
 * Cleanup sentinel. Every row inserted by this harness carries this string as
 * `created_by` (production_orders) and `imported_by` (import_batches). The
 * cleanup WHERE clause is `eq(col, 'harness-xlsx-test')` — no other rows are
 * ever touched.
 *
 * production_orders.createdBy   = 'harness-xlsx-test'  (insert + cleanup WHERE)
 * order_events.changedBy        = 'harness-xlsx-test'  (insert + cleanup WHERE)
 * import_batches.importedBy     = 'harness-xlsx-test'  (insert + cleanup WHERE)
 */
const HARNESS_CREATED_BY = 'harness-xlsx-test';

/**
 * Path to the Book1.xlsx fixture. Resolved relative to the script file so it
 * works regardless of the CWD from which npm run test:xlsx-import is invoked.
 * __dirname is available because tsx compiles to CJS internally.
 */
const FIXTURE_PATH = path.resolve(__dirname, '../example-data/Book1.xlsx');

// ── XLSX Schema ──────────────────────────────────────────────────────────────
// MUST match src/actions/import.ts xlsxSchema verbatim (lines 66-76).
// If the column mapping changes in the action, update this copy and add a
// comment referencing the action commit that changed it.
// eslint-disable-next-line @typescript-eslint/no-explicit-any
const xlsxSchema: Record<string, any> = {
  'Document Number': { prop: 'orderNumber', type: String, required: true },
  Customer:          { prop: 'customer', type: String, required: true },
  Product:           { prop: 'product', type: String, required: true },
  Weight:            { prop: 'weightLbs', type: Number, required: true },
  'Early Delivery Date': { prop: 'deliveryDate', type: Date },
  'Formula Type':    { prop: 'formulaType', type: String, required: true },
  'Texture Type':    { prop: 'textureType', type: String },
  'Line Code':       { prop: 'lineCode', type: String },
  // No 'Mill Line' key — Book1.xlsx has no Mill Line column (D-16)
};

// ── Type definitions ─────────────────────────────────────────────────────────

interface ParsedRow {
  orderNumber: string;
  customer: string;
  product: string;
  weightLbs: number;
  deliveryTime: string;
  formulaType: string;
  millLine: 'Premix' | 'Excel' | 'CGM';
  textureType: string | null;
  lineCode: string | null;
}

// ── Helper: Date → YYYY-MM-DD ────────────────────────────────────────────────
// Mirrors the private `dateToIsoString` helper in src/actions/import.ts (line 93).
function dateToIsoString(d: unknown): string {
  if (d instanceof Date && !isNaN(d.getTime())) {
    return d.toISOString().split('T')[0];
  }
  return '';
}

// ── Step 1: loadFixture ───────────────────────────────────────────────────────
function loadFixture(): Buffer {
  console.log('step 1: loadFixture ...');
  let buffer: Buffer;
  try {
    buffer = readFileSync(FIXTURE_PATH);
  } catch (err) {
    throw new Error(`Cannot read fixture at ${FIXTURE_PATH}: ${String(err)}`);
  }

  // IMPORT-07 layer 2: same byte-size guard as the action (T-33-DoS)
  if (buffer.byteLength > MAX_IMPORT_BYTES) {
    throw new Error(
      `Fixture is ${buffer.byteLength} bytes — exceeds MAX_IMPORT_BYTES (${MAX_IMPORT_BYTES}). ` +
      'Use a smaller fixture for the harness test.'
    );
  }

  console.log(`step 1: loadFixture - OK (${buffer.byteLength} bytes)`);
  return buffer;
}

// ── Step 2: parse ─────────────────────────────────────────────────────────────
async function parse(buffer: Buffer): Promise<ParsedRow[]> {
  console.log('step 2: parse ...');

  // Type cast mirrors src/actions/import.ts lines 123-128 exactly.
  type XlsxFn = (
    input: Buffer,
    options: { schema: Record<string, unknown> }
  ) => Promise<{
    rows: Record<string, unknown>[];
    errors: Array<{ row: number; column: string; error: string; value: unknown }>;
  }>;

  const { rows: rawRows, errors: parserErrors } = await (readXlsxFile as unknown as XlsxFn)(
    buffer,
    { schema: xlsxSchema }
  );

  if (parserErrors.length > 0) {
    console.warn(`  parse: ${parserErrors.length} parser error(s) in fixture:`);
    for (const pe of parserErrors) {
      console.warn(`    row ${pe.row} col "${pe.column}": ${pe.error} (value: ${String(pe.value)})`);
    }
  }

  const validRows: ParsedRow[] = [];

  for (let i = 0; i < rawRows.length; i++) {
    const raw = rawRows[i];
    // D-16: inject 'Premix' + date conversion before Zod validation
    const toValidate = {
      ...raw,
      millLine: 'Premix' as const,
      deliveryTime: dateToIsoString(raw.deliveryDate),
    };

    const parsed = productionOrderImportSchema.safeParse(toValidate);
    if (!parsed.success) {
      console.warn(`  parse: row ${i + 1} failed validation:`, parsed.error.issues);
      continue; // skip invalid rows — harness uses only clean rows
    }

    validRows.push({
      orderNumber: parsed.data.orderNumber,
      customer: parsed.data.customer,
      product: parsed.data.product,
      weightLbs: parsed.data.weightLbs,
      deliveryTime: parsed.data.deliveryTime,
      formulaType: parsed.data.formulaType,
      millLine: parsed.data.millLine,
      textureType: parsed.data.textureType ?? null,
      lineCode: parsed.data.lineCode ?? null,
    });
  }

  console.log(`step 2: parse - OK (${validRows.length} valid rows from ${rawRows.length} raw rows)`);
  return validRows;
}

// ── Step 3: preDeleteSentinelRows ─────────────────────────────────────────────
async function preDeleteSentinelRows(): Promise<void> {
  console.log('step 3: preDeleteSentinelRows ...');

  // Delete in order: order_events (FK cascade from production_orders), then
  // production_orders, then import_batches. Explicit event delete is belt-and-
  // suspenders — the CASCADE on the FK handles it automatically, but being
  // explicit confirms the FK cascade is wired correctly.
  /* eslint-disable @typescript-eslint/no-explicit-any */
  const eventsDeleted = await db.delete(orderEvents).where(eq(orderEvents.changedBy, HARNESS_CREATED_BY)).returning({ id: orderEvents.id });
  const ordersDeleted = await db.delete(productionOrders).where(eq(productionOrders.createdBy, HARNESS_CREATED_BY)).returning({ id: productionOrders.id });
  const batchesDeleted = await db.delete(importBatches).where(eq(importBatches.importedBy, HARNESS_CREATED_BY)).returning({ id: importBatches.id });
  /* eslint-enable @typescript-eslint/no-explicit-any */

  console.log(
    `step 3: preDeleteSentinelRows - deleted ${ordersDeleted.length} production_orders, ` +
    `${batchesDeleted.length} import_batches, ${eventsDeleted.length} order_events`
  );
}

// ── Step 4: snapshotCounts ────────────────────────────────────────────────────
interface Snapshot {
  ordersCount: number;
  batchesCount: number;
}

async function snapshotCounts(): Promise<Snapshot> {
  console.log('step 4: snapshotCounts ...');

  const [ordersResult] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(productionOrders);

  const [batchesResult] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(importBatches);

  const snapshot: Snapshot = {
    ordersCount: ordersResult?.count ?? 0,
    batchesCount: batchesResult?.count ?? 0,
  };

  console.log(
    `step 4: snapshot - prod_orders=${snapshot.ordersCount}, import_batches=${snapshot.batchesCount}`
  );
  return snapshot;
}

// ── Step 5: commit ────────────────────────────────────────────────────────────
interface CommitStats {
  committedCount: number;
  failedCount: number;
  batchId: string;
}

async function commit(parsedRows: ParsedRow[]): Promise<CommitStats> {
  console.log('step 5: commit ...');

  const batchId = randomUUID();
  let committedCount = 0;
  let failedCount = 0;

  for (const row of parsedRows) {
    // D-08: per-row auto-commit; no transaction wrapper (neon-http limitation)
    try {
      // INSERT productionOrders
      // CR-01: weightLbs.toString() — numeric column expects string in Drizzle
      // D-16: millLine defaults to 'Premix'
      // D-13: state defaults to 'Pending' for all new imports
      const [inserted] = await db
        .insert(productionOrders)
        .values({
          orderNumber: row.orderNumber,
          customer: row.customer,
          product: row.product,
          weightLbs: row.weightLbs.toString(), // CR-01: numeric→string at DB boundary
          deliveryTime: row.deliveryTime,
          millLine: 'Premix' as const,          // D-16
          textureType: row.textureType ?? null,
          lineCode: row.lineCode ?? null,
          state: 'Pending' as const,             // D-13
          version: 1,
          createdBy: HARNESS_CREATED_BY,         // sentinel for cleanup
        })
        .returning({ id: productionOrders.id });

      // Audit row: initial-insert event (RESEARCH.md Open Question 2 — YES)
      // fromState: null (no prior state — row is being created for the first time)
      // toState: 'Pending' (initial state)
      await db.insert(orderEvents).values({
        orderId: inserted.id,
        fromState: null,
        toState: 'Pending',
        changedBy: HARNESS_CREATED_BY,   // sentinel for cleanup
        note: 'Imported from XLSX',
      });

      committedCount++;
    } catch (err) {
      // D-08: partial-import — failed rows do not abort the loop
      console.error(`  commit: row "${row.orderNumber}" failed:`, String(err));
      failedCount++;
    }
  }

  // D-07: write import_batches row ONLY if committedCount > 0
  if (committedCount > 0) {
    await db.insert(importBatches).values({
      id: batchId,
      fileName: 'Book1.xlsx',
      rowCount: committedCount,
      importedBy: HARNESS_CREATED_BY,   // sentinel for cleanup
    });
  }

  console.log(`step 5: commit - committed=${committedCount}, failed=${failedCount}`);
  return { committedCount, failedCount, batchId };
}

// ── Step 6: assertPostConditions ──────────────────────────────────────────────
async function assertPostConditions(
  snapshot: Snapshot,
  stats: CommitStats
): Promise<boolean> {
  console.log('step 6: assertPostConditions ...');

  let passed = true;

  // Assert production_orders delta === committedCount
  const [ordersResult] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(productionOrders);
  const newOrdersCount = ordersResult?.count ?? 0;
  const ordersDelta = newOrdersCount - snapshot.ordersCount;

  if (ordersDelta !== stats.committedCount) {
    console.error(
      `  FAIL: production_orders delta=${ordersDelta} but expected committedCount=${stats.committedCount}`
    );
    passed = false;
  } else {
    console.log(`  OK: production_orders delta=${ordersDelta} === committedCount=${stats.committedCount}`);
  }

  if (stats.committedCount > 0) {
    // Assert import_batches delta === 1
    const [batchesResult] = await db
      .select({ count: sql<number>`count(*)::int` })
      .from(importBatches);
    const newBatchesCount = batchesResult?.count ?? 0;
    const batchesDelta = newBatchesCount - snapshot.batchesCount;

    if (batchesDelta !== 1) {
      console.error(
        `  FAIL: import_batches delta=${batchesDelta} but expected 1`
      );
      passed = false;
    } else {
      console.log(`  OK: import_batches delta=${batchesDelta} === 1`);
    }

    // Assert the inserted batch row has correct fields
    const batches = await db
      .select()
      .from(importBatches)
      .where(eq(importBatches.importedBy, HARNESS_CREATED_BY));

    if (batches.length === 0) {
      console.error('  FAIL: no import_batches row found for sentinel');
      passed = false;
    } else {
      const batch = batches[batches.length - 1]; // most recent
      if (batch.fileName !== 'Book1.xlsx') {
        console.error(`  FAIL: batch.fileName="${batch.fileName}" expected "Book1.xlsx"`);
        passed = false;
      } else {
        console.log(`  OK: batch.fileName="${batch.fileName}"`);
      }
      if (batch.rowCount !== stats.committedCount) {
        console.error(
          `  FAIL: batch.rowCount=${batch.rowCount} expected committedCount=${stats.committedCount}`
        );
        passed = false;
      } else {
        console.log(`  OK: batch.rowCount=${batch.rowCount} === committedCount=${stats.committedCount}`);
      }
    }

    // Assert order_events count >= committedCount (one event per inserted order)
    const [eventsResult] = await db
      .select({ count: sql<number>`count(*)::int` })
      .from(orderEvents)
      .where(eq(orderEvents.changedBy, HARNESS_CREATED_BY));
    const eventsCount = eventsResult?.count ?? 0;

    if (eventsCount < stats.committedCount) {
      console.error(
        `  FAIL: order_events count=${eventsCount} < committedCount=${stats.committedCount}`
      );
      passed = false;
    } else {
      console.log(
        `  OK: order_events count=${eventsCount} >= committedCount=${stats.committedCount}`
      );
    }
  }

  console.log(
    `step 6: assertPostConditions - ${passed ? 'PASSED' : 'FAILED'}`
  );
  return passed;
}

// ── Step 7: cleanup ──────────────────────────────────────────────────────────
// Called from the `finally` block — must succeed even after assertion failures.
async function cleanup(): Promise<void> {
  try {
    console.log('step 7: cleanup ...');

    // Delete in FK-safe order: events first, then orders, then batches.
    const eventsDeleted = await db.delete(orderEvents).where(eq(orderEvents.changedBy, HARNESS_CREATED_BY)).returning({ id: orderEvents.id });
    const ordersDeleted = await db.delete(productionOrders).where(eq(productionOrders.createdBy, HARNESS_CREATED_BY)).returning({ id: productionOrders.id });
    const batchesDeleted = await db.delete(importBatches).where(eq(importBatches.importedBy, HARNESS_CREATED_BY)).returning({ id: importBatches.id });

    console.log(
      `step 7: cleanup - deleted ${ordersDeleted.length} production_orders, ` +
      `${batchesDeleted.length} import_batches, ${eventsDeleted.length} order_events`
    );
  } catch (err) {
    console.error('step 7: cleanup FAILED (residue may remain):', String(err));
  }
}

// ── Step 8: main ─────────────────────────────────────────────────────────────
async function main(): Promise<void> {
  const buffer = loadFixture();
  const parsedRows = await parse(buffer);

  if (parsedRows.length === 0) {
    throw new Error('No valid rows parsed from fixture — cannot proceed with commit step.');
  }

  await preDeleteSentinelRows();
  const snapshot = await snapshotCounts();
  const stats = await commit(parsedRows);
  const passed = await assertPostConditions(snapshot, stats);

  if (!passed) {
    throw new Error('One or more post-condition assertions failed — see errors above.');
  }
}

// ── Top-level orchestration ──────────────────────────────────────────────────
(async () => {
  try {
    await main();
    console.log('PASS: GAP-03 closed');
    process.exit(0);
  } catch (err) {
    console.error('FAIL:', err);
    process.exitCode = 1;
  } finally {
    await cleanup();
  }
})();
