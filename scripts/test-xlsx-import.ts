/**
 * scripts/test-xlsx-import.ts
 *
 * End-to-end dev-DB smoke harness for the XLSX import pipeline.
 * Exercises the real `@neondatabase/serverless` HTTP driver against
 * `example-data/Book1.xlsx` and asserts production_orders +
 * import_batches row count deltas.
 *
 * GAP-03 closure (33-VERIFICATION.md): Unit tests mock `@/db`; this harness
 * hits the real Neon HTTP driver so per-row auto-commit behavior and the
 * UNIQUE constraint surface on `order_number` are exercised end-to-end.
 *
 * Usage:
 *   npm run test:xlsx-import
 *   (or: npx tsx scripts/test-xlsx-import.ts)
 *
 * Prerequisites:
 *   - `.env.local` must contain `DATABASE_URL` pointing to the Neon POOLED
 *     endpoint (-pooler.neon.tech). This is the same endpoint used by the
 *     dev server (set since Phase 31).
 *   - DO NOT run against a production DATABASE_URL without first reviewing
 *     the sentinel cleanup (T-33-07-WrongDB threat — accepted for local dev).
 *
 * Why this harness duplicates action logic instead of invoking the action:
 *   The action functions (`previewImportAction`, `commitImportAction`) call
 *   `requireRole('mill_operator')` as their first statement, which in turn
 *   calls `auth()` from `@clerk/nextjs/server`. In a Node CLI context there
 *   is no HTTP request, no Clerk session cookie, and no request-scoped
 *   context object — `auth()` throws. Stubbing Clerk's `auth()` without a
 *   Next.js request harness (e.g. via @clerk/testing setupClerkTestingToken)
 *   requires the full Clerk testing infrastructure and a running Next.js
 *   process, violating scope discipline (plan 33-07 adds ONE script and ONE
 *   npm entry). The gap (GAP-03) is about driver behavior, NOT authorization
 *   semantics — the auth contract is verified by unit tests (TRANS-13 in
 *   33-06-SUMMARY.md). Direct Drizzle calls replicate the exact same SQL
 *   generated by the action and fully exercise the Neon HTTP driver path
 *   (T-33-07-NoAuth disposition: accept).
 *
 * HARNESS_CREATED_BY sentinel rationale:
 *   Every harness-inserted row sets `created_by = 'harness-xlsx-test'`.
 *   The cleanup WHERE clause uses the same sentinel as its sole filter, so
 *   production rows (seeded via 'system-seed' or real Clerk user IDs) are
 *   NEVER touched by this harness.
 *
 * Idempotency contract:
 *   Step 3 (preDeleteSentinelRows) runs BEFORE the insert loop to clear any
 *   leftover state from a prior interrupted run. The `finally` block re-runs
 *   the same deletes so every successful (and failed) run leaves zero residue.
 *
 * Decision references:
 *   - GAP-03: 33-VERIFICATION.md — live Neon HTTP driver gap
 *   - D-08:   Per-row auto-commit; no multi-statement transaction on neon-http.
 *             Partial-import is the contracted behavior (IMPORT-04).
 *   - CR-01:  weightLbs is `numeric(10,2)` on Drizzle — insert as string.
 *   - D-16:   Book1.xlsx has no Mill Line column; every row defaults to 'Premix'.
 *   - D-13:   All imports start as 'Pending'.
 */

